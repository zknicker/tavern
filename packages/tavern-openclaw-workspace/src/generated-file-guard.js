import fs from 'node:fs';
import path from 'node:path';

const generatedInstructionFile = 'AGENTS.md';
const generatedInstructionMarker = 'This AGENTS.md file is generated by Tavern.';
const writeToolNames = new Set([
    'apply_patch',
    'edit',
    'write',
    'write_file',
    'filesystem.write_file',
    'filesystem.edit_file',
]);

export function registerGeneratedFileGuard(api, options = {}) {
    if (typeof api?.on !== 'function') {
        return;
    }

    const generatedInstructionPath = resolveGeneratedInstructionPath(options.workspaceDir);

    api.on(
        'before_tool_call',
        async (event) => {
            if (!targetsGeneratedInstructionFile(event, { generatedInstructionPath })) {
                return undefined;
            }

            return {
                block: true,
                blockReason:
                    'Tavern generates AGENTS.md. Use workspace_notes_update for agent-authored notes.',
            };
        },
        { priority: 100 }
    );
}

export function targetsGeneratedInstructionFile(event, options = {}) {
    const toolName = normalize(event?.toolName);
    if (!(toolName && isPotentialWriteTool(toolName))) {
        return false;
    }

    const generatedInstructionPath =
        resolveGeneratedInstructionPath(options.workspaceDir) ?? options.generatedInstructionPath;

    const cwd = readWorkingDirectory(event);
    for (const candidate of listPathCandidates(event)) {
        if (isGeneratedInstructionPath(candidate, { cwd, generatedInstructionPath })) {
            return true;
        }
    }

    return commandWritesGeneratedInstructions(event?.params, {
        cwd,
        generatedInstructionPath,
    });
}

function isPotentialWriteTool(toolName) {
    return (
        writeToolNames.has(toolName) ||
        toolName.includes('patch') ||
        toolName.includes('edit') ||
        toolName.includes('write') ||
        toolName === 'exec' ||
        toolName === 'process'
    );
}

function listPathCandidates(event) {
    const candidates = [];
    const derivedPaths = event?.derivedPaths;
    if (Array.isArray(derivedPaths)) {
        candidates.push(...derivedPaths);
    }

    const params = event?.params;
    if (params && typeof params === 'object') {
        for (const key of ['path', 'file', 'filePath', 'file_path', 'filename', 'name']) {
            const value = params[key];
            if (typeof value === 'string') {
                candidates.push(value);
            }
        }
    }

    return candidates;
}

function isGeneratedInstructionPath(value, options) {
    const text = typeof value === 'string' ? value.trim() : '';
    if (!text) {
        return false;
    }
    const candidatePath = normalizePath(resolveCandidatePath(text, options.cwd));
    if (!candidatePath) {
        return false;
    }

    return (
        candidatePath === options.generatedInstructionPath ||
        isMarkedGeneratedInstructionPath(candidatePath)
    );
}

function commandWritesGeneratedInstructions(params, options) {
    if (!(params && typeof params === 'object')) {
        return false;
    }

    for (const key of ['cmd', 'command', 'script']) {
        const value = params[key];
        if (typeof value === 'string' && isWriteCommandForGeneratedInstructions(value, options)) {
            return true;
        }
    }

    return false;
}

function isWriteCommandForGeneratedInstructions(command, options) {
    if (!isWriteShapedCommand(command)) {
        return false;
    }

    return listCommandPathCandidates(command).some((candidate) =>
        isGeneratedInstructionPath(candidate, options)
    );
}

function isWriteShapedCommand(command) {
    return /(?:>|>>|\btee\b|\btouch\b|\btruncate\b|\bmv\b|\bcp\b|\brm\b|\bsed\s+-i\b|\bperl\s+-i\b)/u.test(
        command
    );
}

function listCommandPathCandidates(command) {
    return Array.from(command.matchAll(/(?:^|[\s>"'])([^\s>"']*AGENTS\.md)(?=$|[\s<>"'])/gu)).map(
        (match) => match[1]
    );
}

function isMarkedGeneratedInstructionPath(candidatePath) {
    if (path.basename(candidatePath) !== generatedInstructionFile) {
        return false;
    }

    try {
        return fs.readFileSync(candidatePath, 'utf8').includes(generatedInstructionMarker);
    } catch {
        return false;
    }
}

function normalize(value) {
    return typeof value === 'string' ? value.trim().toLowerCase() : null;
}

function resolveGeneratedInstructionPath(workspaceDir) {
    return typeof workspaceDir === 'string' && workspaceDir.trim()
        ? normalizePath(path.resolve(workspaceDir.trim(), generatedInstructionFile))
        : null;
}

function readWorkingDirectory(event) {
    const params = event?.params;
    if (params && typeof params === 'object') {
        for (const key of ['cwd', 'workdir', 'workingDirectory', 'working_directory']) {
            const value = params[key];
            if (typeof value === 'string' && value.trim()) {
                return value.trim();
            }
        }
    }

    return null;
}

function resolveCandidatePath(candidate, cwd) {
    if (path.isAbsolute(candidate)) {
        return candidate;
    }
    if (cwd) {
        return path.resolve(cwd, candidate);
    }
    return candidate;
}

function normalizePath(value) {
    return typeof value === 'string' && value.trim() ? path.resolve(value.trim()) : null;
}
